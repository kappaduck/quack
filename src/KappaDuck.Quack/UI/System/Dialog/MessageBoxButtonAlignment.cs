namespace KappaDuck.Quack.UI.System.Dialog;

/// <summary>
/// The alignment of the message box buttons.
/// </summary>
public enum MessageBoxButtonAlignment : uint
{
    /// <summary>
    /// Specifies that the buttons should be left-aligned.
    /// </summary>
    Left = 0x00000080u,

    /// <summary>
    /// Specifies that the buttons should be right-aligned.
    /// </summary>
    Right = 0x00000100u
}
